package com.hainiu;

public class Demo02_Constructor {
    private String name;
    private int age;
    public Demo02_Constructor() {
    }

    public Demo02_Constructor(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    @Override
    public String toString() {
        return "Demo02_Constructor{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }

    public void setAge(int age) {
        this.age = age;

    }
}
